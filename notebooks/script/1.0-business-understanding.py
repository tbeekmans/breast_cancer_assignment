# ## Project Context
# 
# **Project:** Breast Cancer Classification – DataOps Individual Assignment
# 
# **Domain Context:** This project is part of a university master’s course in software engineering, focusing on the application of DataOps and machine learning to a real-world dataset. The dataset contains information related to breast cancer cases. The main goal is to explore, preprocess, and model this data to support the classification of breast cancer cases. At this stage, domain knowledge is limited and will be developed further as the project progresses.
# 
# **Primary Interest:** The project aims to build a reproducible data pipeline and a baseline classification model. The focus is on establishing a clear, maintainable workflow for data analysis and model development, rather than achieving state-of-the-art results.
# 
# **Key Constraint:** The project is an individual academic assignment with a limited timeframe and resources. The available domain knowledge is minimal, and understanding the medical context is part of the business understanding phase.
# 
# ## Stakeholders & Users
# 
# **Primary User:**
# - The student (project owner), who is responsible for all stages of the pipeline and analysis.
# 
# **Secondary Users:**
# - Teaching staff, who will review and assess the project.
# - Potential future students or researchers who may reuse or extend the workflow.
# 
# ## Problem Statement
# 
# Breast cancer is a significant health issue, and accurate classification of cases can support better diagnosis and treatment. Manual analysis of medical data is time-consuming and may be inconsistent. This project aims to create a reproducible workflow for classifying breast cancer cases using the provided dataset. The focus is on building a transparent and well-documented process, not on clinical deployment.
